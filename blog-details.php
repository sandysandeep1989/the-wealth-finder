          <div class="blogdetail-section">
            <h2>What is IEPF?</h2>
            <p>The Investor Education and Protection Fund (IEPF) is a statutory body established by the Government of India under the Companies Act, 2013. It serves as a repository for unclaimed dividends, matured deposits, debentures, and shares that have remained unclaimed for seven consecutive years or more.</p>
            <p>The primary objective of IEPF is to protect the interests of investors and promote awareness about the capital markets. When investors fail to claim their rightful assets within the stipulated time frame, these assets are transferred to the IEPF for safekeeping.</p>
          </div>

          <div class="blogdetail-callout blogdetail-callout-blue">
            <h4>Key Fact</h4>
            <p>As of 2026, over ₹25,000 crores worth of assets are held in IEPF, waiting to be claimed by rightful owners.</p>
          </div>

          <div class="blogdetail-section">
            <h2>Why Are Assets Transferred to IEPF?</h2>
            <p>Assets are transferred to IEPF for several reasons:</p>
            <ul>
              <li>Unclaimed dividends: When dividends remain unclaimed for 7 consecutive years</li>
              <li>Inactive accounts: Shares in demat accounts that haven't been accessed for 7 years</li>
              <li>Matured deposits: Fixed deposits or bonds that have matured but haven't been claimed</li>
              <li>Lost communication: Investors who have changed addresses without updating their records</li>
              <li>Deceased shareholders: Assets of deceased shareholders where legal heirs are unaware</li>
            </ul>
          </div>

          <div class="blogdetail-section">
            <h2>Who Can Claim from IEPF?</h2>
            <p>The following individuals are eligible to file claims with IEPF:</p>
            <ul>
              <li>Original shareholders or investors whose assets were transferred</li>
              <li>Legal heirs of deceased shareholders (with proper succession documents)</li>
              <li>Power of attorney holders with valid authorization</li>
              <li>NRIs and OCIs with unclaimed assets in India</li>
              <li>Companies claiming on behalf of their shareholders (in specific cases)</li>
            </ul>
          </div>

          <div class="blogdetail-callout blogdetail-callout-orange">
            <h4>Important Notice</h4>
            <p>Claims must be filed within the limitation period as per applicable laws. Delayed claims may face additional scrutiny.</p>
          </div>

          <div class="blogdetail-section">
            <h2>The Recovery Process</h2>
            <p>The IEPF recovery process follows a structured approach:</p>
            <ol>
              <li>Search &amp; Verification: Verify if your assets are in IEPF using the official portal</li>
              <li>Document Preparation: Gather all required documents (details below)</li>
              <li>Online Form Filing: Submit Form IEPF-5 through the MCA portal</li>
              <li>Physical Documents: Send notarized documents to IEPF Authority</li>
              <li>Company Verification: The company verifies your claim</li>
              <li>IEPF Approval: IEPF Authority reviews and approves the claim</li>
              <li>Asset Transfer: Shares credited to demat or refund processed</li>
            </ol>
            <p>The typical processing time ranges from 3 to 6 months, depending on the complexity of the case and completeness of documentation.</p>
          </div>

          <div class="blogdetail-section">
            <h2>Required Documents</h2>
            <p>To successfully claim your assets from IEPF, you'll need to submit:</p>

            <h3>For Individual Claimants:</h3>
            <ul>
              <li>PAN Card (mandatory)</li>
              <li>Aadhaar Card</li>
              <li>Cancelled cheque or bank statement</li>
              <li>Original share certificates (if physical shares)</li>
              <li>Demat account details</li>
              <li>Proof of address</li>
              <li>Self-attested indemnity bond</li>
            </ul>

            <h3>For Legal Heirs:</h3>
            <ul>
              <li>All individual claimant documents</li>
              <li>Death certificate of the deceased shareholder</li>
              <li>Succession certificate or Legal heir certificate</li>
              <li>Notarized affidavit</li>
              <li>Family tree with nominee details</li>
            </ul>
          </div>

          <div class="blogdetail-section">
            <h2>Common Challenges</h2>
            <p>Investors often face several challenges during the IEPF recovery process:</p>

            <h3>1. Documentation Issues</h3>
            <p>Missing or incomplete documents are the most common reason for claim rejection. Ensure all documents are properly attested and notarized as required.</p>

            <h3>2. Company Non-Cooperation</h3>
            <p>Some companies are slow to respond or verify claims. In such cases, follow up persistently and escalate to IEPF if needed.</p>

            <h3>3. Technical Difficulties</h3>
            <p>The MCA portal can be complex for first-time users. Consider seeking professional assistance if you're not comfortable with online filing.</p>

            <h3>4. Legal Heir Disputes</h3>
            <p>When multiple legal heirs claim the same assets, proper succession documents and family consent are crucial.</p>
